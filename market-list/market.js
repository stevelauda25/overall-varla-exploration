/* ---- Combined filter state: category chip + search query + segment + sort.
   All apply at the same time — a card is shown only if it matches all.
   Declared early so render() can read `filterState.sort` on first paint. */
const filterState = { category: "all", query: "", inWallet: false, segment: "supported", sort: "newest" };

/* Volume strings ("$4.8M", "$890K", "$697K") → number, for sort comparison. */
function parseVolume(s) {
  if (!s) return 0;
  const m = s.match(/\$([\d.]+)\s*([KMB]?)/i);
  if (!m) return 0;
  const num = parseFloat(m[1]) || 0;
  const mult = { K: 1e3, M: 1e6, B: 1e9 }[m[2].toUpperCase()] || 1;
  return num * mult;
}

/* timeLeft strings ("8d 4h left", "29d left", "5mo left", "1y 2mo left")
   → minutes-remaining number. Markets without timeLeft (e.g. unsupported)
   are sorted last via Number.MAX_SAFE_INTEGER. */
function parseTimeLeft(s) {
  if (!s) return Number.MAX_SAFE_INTEGER;
  const factors = { y: 525600, mo: 43200, d: 1440, h: 60, m: 1 };
  let total = 0;
  // `mo` is matched before `m` so "5mo" doesn't read as "5m + o".
  for (const [, n, u] of s.matchAll(/(\d+)\s*(mo|y|d|h|m)/g)) {
    total += parseInt(n, 10) * (factors[u] || 0);
  }
  return total || Number.MAX_SAFE_INTEGER;
}

/* Return a copy of `arr` ordered per the active sort. The original array
   stays untouched so switching back to "Newest" restores natural order. */
function getSortedMarkets(arr) {
  const sort = filterState.sort;
  if (sort === "newest" || !sort) return arr;
  const copy = [...arr];
  if (sort === "oldest") return copy.reverse();
  if (sort === "volume") {
    return copy.sort((a, b) => parseVolume(b.volume) - parseVolume(a.volume));
  }
  if (sort === "ending-soon") {
    return copy.sort((a, b) => parseTimeLeft(a.timeLeft) - parseTimeLeft(b.timeLeft));
  }
  return arr;
}

/** Wire up a custom scroll-thumb overlay for an option list.
 *  Sizes and positions the thumb based on scroll progress, hides it when
 *  the list doesn't overflow, and flashes `is-visible` while the user is
 *  actively scrolling so the thumb appears even without hovering. */
function attachScrollThumb(list, thumb) {
  if (!list || !thumb) return;
  let hideTimer = null;

  const update = () => {
    const visible = list.clientHeight;
    const total = list.scrollHeight;
    if (total <= visible + 1) {
      thumb.hidden = true;
      return;
    }
    thumb.hidden = false;
    const thumbHeight = Math.max((visible / total) * visible, 16);
    const maxScroll = total - visible;
    const range = visible - thumbHeight;
    const top = maxScroll > 0 ? (list.scrollTop / maxScroll) * range : 0;
    thumb.style.height = `${thumbHeight}px`;
    thumb.style.transform = `translateY(${top}px)`;
  };

  list.addEventListener("scroll", () => {
    update();
    thumb.classList.add("is-visible");
    clearTimeout(hideTimer);
    hideTimer = setTimeout(() => thumb.classList.remove("is-visible"), 700);
  });

  // Recompute on size changes (e.g. font load, layout shifts).
  if (typeof ResizeObserver !== "undefined") {
    new ResizeObserver(update).observe(list);
  }
  update();
}

