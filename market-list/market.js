/* =========================================================================
   Wallet tooltip — opens on click of the help icon, closes on close-btn
   click, "Got it"/"Skip" click, outside click, or Escape key.
   Positioned with `position: fixed` based on the trigger's bounding rect
   so it escapes the sticky toolbar's clipping context.
   ========================================================================= */
const tooltip = document.getElementById("wallet-tooltip");
const tooltipTrigger = document.querySelector("[data-tooltip-trigger]");

function openTooltip() {
  if (!tooltip || !tooltipTrigger) return;
  const rect = tooltipTrigger.getBoundingClientRect();
  tooltip.hidden = false;
  // Position below the trigger, right-aligned to the icon. Re-measure
  // tooltip dimensions after it's visible so we can clamp inside the viewport.
  const ttRect = tooltip.getBoundingClientRect();
  const margin = 8;
  let left = rect.right - ttRect.width;
  let top = rect.bottom + 8;
  // Clamp to viewport
  left = Math.max(margin, Math.min(left, window.innerWidth - ttRect.width - margin));
  // If not enough room below, flip above
  if (top + ttRect.height > window.innerHeight - margin) {
    top = rect.top - ttRect.height - 8;
  }
  tooltip.style.left = `${left}px`;
  tooltip.style.top = `${top}px`;
}

function closeTooltip() {
  if (tooltip) tooltip.hidden = true;
}

